int	is_digit(int c)
{
	if ('0' <= c && c <= '9')
		return (1);
	return (0);
}

int	is_sign(char c)
{
	return (c == '-' || c == '+');
}

int	is_whitespace(char c)
{
	return (c == '\t' || c == '\n' || c == '\v' || c == '\f' || c == '\r'
		|| c == ' ');
}
