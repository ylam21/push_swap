#include "../includes/libft.h"

size_t	ft_strlen(const char *s)
{
	size_t			len;
	unsigned int	i;

	len = 0;
	i = 0;
	while (s && s[i] != '\0')
	{
		len++;
		i++;
	}
	return (len);
}
