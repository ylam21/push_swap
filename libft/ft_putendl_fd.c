#include "libft.h"

void	ft_putendl_fd(char *s, int fd)
{
	int		i;
	char	newl;

	newl = '\n';
	i = 0;
	while (s && s[i] != '\0')
		write(fd, &s[i++], 1);
	write(fd, &newl, 1);
}
